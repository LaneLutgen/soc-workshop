int main(int argc, char *argv[])
{
	DIR *sysfs_dir;
	struct dirent *uio_dirent;
	int result;
	char uio_dev_name[256];
	int uio_dev_fd;
	void *uio_driver_map;
	void *rom_ptr;
	void *ram_ptr;
	volatile unsigned long *tmr_ptr;
	unsigned long irq_control;
	unsigned long irq_count_0;
	unsigned long irq_count_1;
	unsigned long irq_count_2;
	unsigned long irq_count_3;
	double f_timeout_period = (double)(0.010) / ((double)(1) / (double)(TIMER_FREQ));
	unsigned long timeout_period = f_timeout_period;
	
	argv = argv;
	
	/*
	 * validate proper commandline construction
	 */
	if (argc != 1)
		error_at_line(1, 0, __FILE__, __LINE__, "program expects no arguments");
	
	/*
	 * open the expected sysfs directory and read the uioX allocation for our device
	 */
	sysfs_dir = opendir(SYS_UIO_DIR);
	if (sysfs_dir == NULL)
		error_at_line(1, errno, __FILE__, __LINE__, "while opening directory '%s'", SYS_UIO_DIR);
	
	do {
		uio_dirent = readdir(sysfs_dir);
		if (uio_dirent == NULL)
			error_at_line(1, errno, __FILE__, __LINE__, "while reading directory '%s'", SYS_UIO_DIR);
	} while(strncmp(uio_dirent->d_name, "uio", 3));	

	result = closedir(sysfs_dir);
	if (result != 0)
		error_at_line(1, errno, __FILE__, __LINE__, "while closing directory '%s'", SYS_UIO_DIR);

	/*
	 * open the uio device node
	 */
	result = snprintf(uio_dev_name, 256, "/dev/%s", uio_dirent->d_name);
	if (result >= 256)
		error_at_line(1, 0, __FILE__, __LINE__, "directory name too long '%s'", SYS_UIO_DIR);
	
	uio_dev_fd = open(uio_dev_name, O_RDWR | O_SYNC);
	if (uio_dev_fd == -1)
		error_at_line(1, errno, __FILE__, __LINE__, "while opening '%s'", uio_dev_name);

	/*
	 * mmap our peripheral address span
	 */
	uio_driver_map = mmap(NULL, sysconf(_SC_PAGE_SIZE), PROT_READ|PROT_WRITE, MAP_SHARED, uio_dev_fd, 0);
	if(uio_driver_map == MAP_FAILED)
		error_at_line(1, errno, __FILE__, __LINE__, "while mmap'ing '%s'", uio_dev_name);

	rom_ptr = (void *)((unsigned long)uio_driver_map + ROM_OFST);
	ram_ptr = (void *)((unsigned long)uio_driver_map + RAM_OFST);
	tmr_ptr = (unsigned long *)((unsigned long)uio_driver_map + TIMER_OFST);

	/*
	 * fill ram with default pattern and verify
	 */
	memcpy(ram_ptr, g_ram_default_contents, RAM_SPAN);

	result = memcmp(ram_ptr, g_ram_default_contents, RAM_SPAN);
	if (result != 0)
		error_at_line(1, 0, __FILE__, __LINE__, "ram verify default failed");

	/*
	 * verify rom data
	 */
	result = memcmp(rom_ptr, g_rom_default_contents, ROM_SPAN);
	if (result != 0)
		error_at_line(1, 0, __FILE__, __LINE__, "rom verify default failed");

	/*
	 * fill ram with rom pattern and verify
	 */
	memcpy(ram_ptr, rom_ptr, RAM_SPAN);

	result = memcmp(ram_ptr, rom_ptr, RAM_SPAN);
	if (result != 0)
		error_at_line(1, 0, __FILE__, __LINE__, "ram verify rom failed");

	/*
	 * stop the timer
	 */
	tmr_ptr[ALTERA_AVALON_TIMER_CONTROL_REG] = ALTERA_AVALON_TIMER_CONTROL_STOP_MSK;
	if ((tmr_ptr[ALTERA_AVALON_TIMER_STATUS_REG] & ALTERA_AVALON_TIMER_STATUS_RUN_MSK) != 0)
		error_at_line(1, 0, __FILE__, __LINE__, "timer won't stop");

	/*
	 * start the timer
	 */
	timeout_period--;
	tmr_ptr[ALTERA_AVALON_TIMER_PERIODL_REG] = timeout_period;
	tmr_ptr[ALTERA_AVALON_TIMER_PERIODH_REG] = timeout_period >> 16;
	tmr_ptr[ALTERA_AVALON_TIMER_CONTROL_REG] = ALTERA_AVALON_TIMER_CONTROL_ITO_MSK | ALTERA_AVALON_TIMER_CONTROL_CONT_MSK | ALTERA_AVALON_TIMER_CONTROL_START_MSK;
	if ((tmr_ptr[ALTERA_AVALON_TIMER_STATUS_REG] & ALTERA_AVALON_TIMER_STATUS_RUN_MSK) != ALTERA_AVALON_TIMER_STATUS_RUN_MSK)
		error_at_line(1, 0, __FILE__, __LINE__, "timer won't start");

	/*
	 * enable timer irq and wait for 4 interrupts
	 */
	irq_control = 1;

	result = write(uio_dev_fd, &irq_control, sizeof(irq_control));
	if (result < 0)
		error_at_line(1, errno, __FILE__, __LINE__, "writing '%s'", uio_dev_name);
	if (result != 4)
		error_at_line(1, 0, __FILE__, __LINE__, "failed to write 4 bytes to '%s'", uio_dev_name);

	result = read(uio_dev_fd, &irq_count_0, 4);
	if (result < 0)
		error_at_line(1, errno, __FILE__, __LINE__, "reading '%s'", uio_dev_name);
	if (result != 4)
		error_at_line(1, 0, __FILE__, __LINE__, "failed to read 4 bytes to '%s'", uio_dev_name);

	tmr_ptr[ALTERA_AVALON_TIMER_STATUS_REG] = 0;

	result = write(uio_dev_fd, &irq_control, sizeof(irq_control));
	if (result < 0)
		error_at_line(1, errno, __FILE__, __LINE__, "writing '%s'", uio_dev_name);
	if (result != 4)
		error_at_line(1, 0, __FILE__, __LINE__, "failed to write 4 bytes to '%s'", uio_dev_name);

	result = read(uio_dev_fd, &irq_count_1, 4);
	if (result < 0)
		error_at_line(1, errno, __FILE__, __LINE__, "reading '%s'", uio_dev_name);
	if (result != 4)
		error_at_line(1, 0, __FILE__, __LINE__, "failed to read 4 bytes to '%s'", uio_dev_name);

	tmr_ptr[ALTERA_AVALON_TIMER_STATUS_REG] = 0;

	result = write(uio_dev_fd, &irq_control, sizeof(irq_control));
	if (result < 0)
		error_at_line(1, errno, __FILE__, __LINE__, "writing '%s'", uio_dev_name);
	if (result != 4)
		error_at_line(1, 0, __FILE__, __LINE__, "failed to write 4 bytes to '%s'", uio_dev_name);

	result = read(uio_dev_fd, &irq_count_2, 4);
	if (result < 0)
		error_at_line(1, errno, __FILE__, __LINE__, "reading '%s'", uio_dev_name);
	if (result != 4)
		error_at_line(1, 0, __FILE__, __LINE__, "failed to read 4 bytes to '%s'", uio_dev_name);

	tmr_ptr[ALTERA_AVALON_TIMER_STATUS_REG] = 0;

	result = write(uio_dev_fd, &irq_control, sizeof(irq_control));
	if (result < 0)
		error_at_line(1, errno, __FILE__, __LINE__, "writing '%s'", uio_dev_name);
	if (result != 4)
		error_at_line(1, 0, __FILE__, __LINE__, "failed to write 4 bytes to '%s'", uio_dev_name);

	result = read(uio_dev_fd, &irq_count_3, 4);
	if (result < 0)
		error_at_line(1, errno, __FILE__, __LINE__, "reading '%s'", uio_dev_name);
	if (result != 4)
		error_at_line(1, 0, __FILE__, __LINE__, "failed to read 4 bytes to '%s'", uio_dev_name);

	tmr_ptr[ALTERA_AVALON_TIMER_STATUS_REG] = 0;

	/*
	 * stop the timer
	 */
	tmr_ptr[ALTERA_AVALON_TIMER_CONTROL_REG] = ALTERA_AVALON_TIMER_CONTROL_STOP_MSK;
	if ((tmr_ptr[ALTERA_AVALON_TIMER_STATUS_REG] & ALTERA_AVALON_TIMER_STATUS_RUN_MSK) != 0)
		error_at_line(1, 0, __FILE__, __LINE__, "timer won't stop");

	/*
	 * verify the irq_count values
	 */
	if ((irq_count_0 + 1) != irq_count_1)
		error_at_line(1, 0, __FILE__, __LINE__, "wrong irq_count sequence 1");
	if ((irq_count_1 + 1) != irq_count_2)
		error_at_line(1, 0, __FILE__, __LINE__, "wrong irq_count sequence 2");
	if ((irq_count_2 + 1) != irq_count_3)
		error_at_line(1, 0, __FILE__, __LINE__, "wrong irq_count sequence 3");

	/*
	 * munmap and close all the file descriptors and exit
	 */
	result = munmap(uio_driver_map, sysconf(_SC_PAGE_SIZE));
	if(result < 0)
		error_at_line(1, errno, __FILE__, __LINE__, "while munmap'ing '%s'", uio_dev_name);

	close(uio_dev_fd);

	printf("Success\nEnd of test...\n");
	exit(EXIT_SUCCESS);
}

